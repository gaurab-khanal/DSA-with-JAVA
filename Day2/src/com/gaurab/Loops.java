package com.gaurab;

import java.util.Scanner;

public class Loops {
    public static void main(String[] args) {
    // For loop
        for (int i= 1; i<=10; i++){
            System.out.println(i);
        }
        // While loop
        int num = 1;
        while(num<=10){
            System.out.println(num);
            num++;
        }
        // do while loop
        int i = 1;
        do{
            System.out.println(i);
            i++;
        }while(i<=10);
    }
    }


